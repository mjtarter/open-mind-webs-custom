<p style="font-size:24px; font-weight:bold; text-align:center;">Maintenance</p><div class="CenterText">
Once your desired web service is completed and launched we don't just set you loose on your own. We offer maintenance and content updates for free through the first month and then offer plans to continue after that. You can continue to change content and make updates without ever having to mess with the code on the back end. However, if you'd prefer to take full control, we can teach you how to make minor updates and changes yourself.
