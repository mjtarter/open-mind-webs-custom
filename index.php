<p style="font-size:24px; font-weight:bold; text-align:center;">Planning / Strategy</p><div class="CenterText">
Whether you are interested in a website, database management system, or other web service, there is no reason to feel overwhelmed at the initial stages. We'll help you to find a starting point and take you through the process of coming up with the look, feel, and content structure that will work best for your particular business. All you need is an idea and we can help you throughout the process of planning to completion.
</div></div>

<div class="Process2">
<p style="font-size:24px; font-weight:bold; text-align:center;">Creation</p><div class="CenterText">
One of the major benefits that Open Mind Webs offers is that we allow you to continuously make revisions to your website or project throughout the entire development process. We realize that what looks good on paper, might not be what you had in mind once you see it on the screen. Whether it's using a different font or content revisions, we work with you throughout the creation phase to ensure that you are satisfied with every single pixel and line of code.
</div></div>

